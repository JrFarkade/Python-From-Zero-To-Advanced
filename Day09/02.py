# A parameter is a variable written inside a function definition.
# It is used to receive values when the function is called.

def greet(name):   # name is parameter
    print("Hello", name)

greet("Sahil")    # Sahil is argument

# name → parameter (inside function)
# "Sahil" → argument (value passed)

