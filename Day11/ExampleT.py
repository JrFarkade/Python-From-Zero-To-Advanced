# Program to demonstrate tuple examples

# Example 1: Store days of week
days = ("Monday", "Tuesday", "Wednesday")

print(days)

# Example 2: Access tuple item
print("First day:", days[0])

# Example 3: Loop through tuple
for day in days:
    print(day)

# Example 4: Tuple with mixed data
student = ("Sahil", 20, "Computer Science")
print(student)
