# Comparison Operators
# Used to compare two values. Output is True or False.

# Operator	Meaning
# ==	        Equal
# !=	        Not equal
# >	          Greater than
# <	          Less than
# >=	        Greater or equal
# <=	        Less or equal

# Example
x = 10
y = 5

print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)
