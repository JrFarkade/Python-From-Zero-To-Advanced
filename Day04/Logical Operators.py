# Logical Operators
# Used to combine conditions.

# Operator	Meaning
# and	        Both conditions must be True
# or	        At least one condition True
# not	        Reverse the result

# Example
# Logical Operators

age = 22
has_id = True

print(age > 18 and has_id)
print(age < 18 or has_id)
print(not has_id)
