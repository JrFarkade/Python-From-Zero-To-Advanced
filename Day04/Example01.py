# 1) Take two numbers and print all arithmetic operations
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print("Add:", a + b)
print("Sub:", a - b)
print("Mul:", a * b)
print("Div:", a / b)

# 2) Check which number is greater
print("a > b:", a > b)

# 3) Logical check
age = int(input("Enter age: "))
print("Eligible for vote:", age >= 18)
