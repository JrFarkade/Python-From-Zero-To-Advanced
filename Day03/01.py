# Day 03 - Data Types in Python
# A data type tells Python what kind of value is stored in a variable.

# int (Integer): Used to store whole numbers.
age = 20

# float(): Used to store decimal numbers.
cgpa = 8.5

# str (String): Used to store text (words/sentences).
OP = "The One Piece Is REAL!!"

# bool (Boolean): Used to store True or False.
is_student = True

# type() function: We use type() to check the data type of a value/variable.
x = 10
print(type(x))

# Type Casting: means converting one data type into another.
a = "10"
print(int(a))     # converts string to int

b = "8.5"
print(float(b))   # converts string to float

c = 100
print(str(c))     # converts int to string
